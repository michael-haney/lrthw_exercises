#!/usr/bin/env ruby

# Setting a variable "types of people" to the value 10.
types_of_people = 10

# Setting variable x to a string including the "types of people" variable, or 10.
x = "There are #{types_of_people} types of people."

# Setting the variable binary to binary.
binary = "binary"

# Setting the variable do_not to the output "don't"
do_not = "don't"

# Setting the variable y to include binary and do_not.
y = "Those who know #{binary} and those who #{do_not}."

# Will print the x variable
puts x

# Will print the y variable
puts y

# Will print the string and the x variable
puts "I said: #{x}."

# Will print the string and the y variable
puts "I also said: '#{y}'."

# Setting the hilarious variable to false
hilarious = false

# Setting the joke evaluation to a string with hilarious variable
joke_evaluation = "Isn't that joke so funny?! #{hilarious}"

# Will print string with hilarious variable
puts joke_evaluation

# Setting the w variable to the following string
w = "This is the left side of..."

# Setting the e variable to the following string
e = "a string with a right side."

# Prints both the w and e strings.
puts w + e
